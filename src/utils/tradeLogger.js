export function parseCSV(text) {
  const lines = text.trim().split('\n');
  if (lines.length < 2) return [];
  const rows = lines.slice(1).map((line, i) => {
    const cols = line.split(',').map(c => c.trim());
    return {
      id: i + 1,
      direction: cols[0]?.toUpperCase() === 'SHORT' ? 'SHORT' : 'LONG',
      entryPrice: cols[1] || '',
      atr: cols[2] || '',
      slMultiple: cols[3] || '1.0',
      tpMultiple: cols[4] || '2.0',
      leverage: cols[5] || '1',
      outcome: cols[6]?.toUpperCase() === 'LOSS' ? 'LOSS' : 'WIN',
    };
  });
  return rows;
}

