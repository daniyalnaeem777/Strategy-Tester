import { calcTrade, applyTradeOutcome } from './calculations.js';

export function createTradeRecord({ tradeNum, direction, entryPrice, atr, slMultiple, tpMultiple, leverage, capital, outcome }) {
  const calc = calcTrade({ direction, entryPrice, atr, slMultiple, tpMultiple, leverage, capital });
  const pnl = outcome === 'WIN' ? calc.maxGain : -calc.maxLoss;
  const capitalAfter = capital + pnl;

  return {
    tradeNum,
    timestamp: Date.now(),
    direction,
    entryPrice: parseFloat(entryPrice),
    atr: parseFloat(atr),
    slMultiple: parseFloat(slMultiple),
    tpMultiple: parseFloat(tpMultiple),
    leverage: parseFloat(leverage),
    slPrice: calc.slPrice,
    tpPrice: calc.tpPrice,
    rr: calc.rr,
    outcome,
    pnl,
    capitalAfter,
    positionSize: calc.positionSize,
    maxLoss: calc.maxLoss,
    maxGain: calc.maxGain,
  };
}

export function exportCSV(trades, sessionName) {
  const headers = ['#', 'Timestamp', 'Direction', 'Entry', 'ATR', 'SL Mult', 'TP Mult', 'Leverage', 'SL Price', 'TP Price', 'R:R', 'Outcome', 'P&L ($)', 'Capital After'];
  const rows = trades.map(t => [
    t.tradeNum,
    new Date(t.timestamp).toISOString(),
    t.direction,
    t.entryPrice,
    t.atr,
    t.slMultiple,
    t.tpMultiple,
    t.leverage,
    t.slPrice?.toFixed(4),
    t.tpPrice?.toFixed(4),
    t.rr?.toFixed(2),
    t.outcome,
    t.pnl?.toFixed(2),
    t.capitalAfter?.toFixed(2),
  ]);

  const csv = [headers, ...rows].map(r => r.join(',')).join('\n');
  const blob = new Blob([csv], { type: 'text/csv' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `TP-SL-Pro-Trades-${sessionName || 'Session'}-${new Date().toISOString().slice(0, 10)}.csv`;
  a.click();
  URL.revokeObjectURL(url);
}

